    private void updateDirectories(Hospital hospital)
    {
        HospitalDirectory hospitalDirectory = DataStore.getInstance().getSystem().getHospitalDirectory();
        
        if( null == hospitalDirectory.getCommunityToHospitals().get(hospital.getCommunity().getCommunityName()) ||
                hospitalDirectory.getCommunityToHospitals().get(hospital.getCommunity().getCommunityName()).isEmpty())
        {
            CopyOnWriteArrayList <Hospital> hospitalList = new CopyOnWriteArrayList <>();
            hospitalList.add(hospital);
            hospitalDirectory.getCommunityToHospitals().put(hospital.getCommunity().getCommunityName(),hospitalList);
        }
        else
        {
            hospitalDirectory.getCommunityToHospitals().get(hospital.getCommunity().getCommunityName()).add(hospital);
        }
        
     //   hospitalDirectory.getCommunityToHospitals().get(hospital.getCommunity().getCommunityName()).add(hospital);
        hospitalDirectory.getHospitalNameToCommunity().put(hospital.getHospitalName(), hospital.getCommunity());
        hospitalDirectory.getHospitalNameToHospital().put(hospital.getHospitalName(),hospital);  
    }
    
