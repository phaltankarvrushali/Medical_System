    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(null == jTextField3.getText() || jTextField3.getText().isEmpty() || null == jTextField4.getText() || jTextField4.getText().isEmpty()
        || null == jTextField5.getText() || jTextField5.getText().isEmpty() || null == jTextField6.getText() || jTextField6.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(jPanel1, "Details cannot be empty"); 
            return;
        }
        
        if(null != hospital)
        {
            JOptionPane.showMessageDialog(jPanel1, "Hospital already exist in the System"); 
            return;
        }
        
        String hospitalName = jTextField1.getText();
        String community = jComboBox1.getSelectedItem().toString();
        String streetname = jTextField3.getText();
        String city = jTextField4.getText();
        String state = jTextField5.getText();
        String zipCode = jTextField6.getText();
        
        String regex = "^[a-zA-Z ]*$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(hospitalName);
        if(!matcher.matches())
        {
            JOptionPane.showMessageDialog(jPanel1, "Invalid Hospital Name"); 
            return;
        }
        
        matcher = pattern.matcher(streetname);
        if(!matcher.matches())
        {
            JOptionPane.showMessageDialog(jPanel1, "Invalid street Name"); 
            return;
        }
        
        matcher = pattern.matcher(city);
        if(!matcher.matches())
        {
            JOptionPane.showMessageDialog(jPanel1, "Invalid city Name"); 
            return;
        }
        
        matcher = pattern.matcher(state);
        if(!matcher.matches())
        {
            JOptionPane.showMessageDialog(jPanel1, "Invalid State"); 
            return;
        }
        
        String regexPattern = "^[0-9]*$";
        pattern = Pattern.compile(regexPattern);
        matcher = pattern.matcher(zipCode);
        if(!matcher.matches())
        {
            JOptionPane.showMessageDialog(jPanel1, "Invalid zipCode"); 
            return;
        }
        
        Hospital hospital = new Hospital();
        hospital.setHospitalName(hospitalName);
        hospital.setCommunity(DataStore.getInstance().getSystem().getCommunityDirectory().getCommunityNameToCommunity().get(community));
        City city1 = new City();
        city1.setCityname(city);
        hospital.setCity(city1);
        hospital.setStreetName(streetname);
        hospital.setState(state);
        hospital.setZipcode(zipCode);
        
        updateDirectories(hospital);
        
        JOptionPane.showMessageDialog(jPanel1, "Hospital Details updated successfully"); 
        dispose();
        new HospitalAdminHospitalList().setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

